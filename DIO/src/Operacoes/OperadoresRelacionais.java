package Operacoes;

public class OperadoresRelacionais {
	public static void main(String[] args) {

		int numero1 = 1;
		int numero2 = 2;

		boolean simNao = numero1 == numero2;

		System.out.println("numeroUm é igual a numeroDois? " + simNao);

		boolean simNao1 = numero1 != numero2;

		System.out.println("numeroUm é diferente a numeroDois? " + simNao1);

		boolean simNao2 = numero1 > numero2;

		System.out.println("numeroUm é maior a numeroDois? " + simNao2);

		boolean simNao3 = numero1 < numero2;

		System.out.println("numeroUm é menor a numeroDois? " + simNao3);

	}
}

//Operadores relacionais são opedarodes de comparaçõs entre valores