package Operacoes;

public class OperadorAritmético {
public static void main(String[] args) {
	 int numero = 5;
	 
	 numero = - numero;
	 System.out.println(numero);
	 
	 numero = numero * -1;
	 System.out.println(numero);
}
	
	
	
	
	
	
	
}

//(+) numeros são de valor positivo sem esse operador explicitamente;
//(-) Nega um numero ou expressão aritmética;
//(++) incrementa o valor positivo a uma unidade;
//(--) incrementa o valor negativo a uma unidade;
//(!) nega o valor de uma expressão booleana;
